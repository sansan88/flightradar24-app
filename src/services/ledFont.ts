/** Umlaute/Diakritika entfernen und unbekannte Zeichen ersetzen */
export function normalizeForLed(text: string): string {
  return [...text
    .replace(/ß/g, 'ss')
    .normalize('NFD')
    .replace(/\p{M}/gu, '')]
    .map((c) => (FONT[c] ? c : c === ' ' ? ' ' : FONT[c.toUpperCase()] ? c.toUpperCase() : '?'))
    .join('');
}

/** Breite eines (bereits normalisierten) Texts in LED-Pixeln */
export function textWidth(text: string): number {
  if (text.length === 0) return 0;
  return text.length * (GLYPH_WIDTH + GLYPH_SPACING) - GLYPH_SPACING;
}

/**
 * Text in das Pixel-Grid der Matrix zeichnen.
 * `grid` ist ein size×size-Buffer; gesetzte Pixel erhalten `colorIndex`.
 */
export function drawTextInto(
  grid: Uint8Array,
  size: number,
  x: number,
  yTop: number,
  text: string,
  colorIndex: number
): void {
  let cx = x;
  for (const char of text) {
    const glyph = FONT[char] ?? FONT['?'];
    for (let col = 0; col < GLYPH_WIDTH; col++) {
      const px = cx + col;
      if (px >= size) return;
      if (px >= 0) {
        const bits = glyph[col];
        for (let row = 0; row < GLYPH_HEIGHT; row++) {
          if (bits & (1 << row)) {
            const py = yTop + row;
            if (py >= 0 && py < size) grid[py * size + px] = colorIndex;
          }
        }
      }
    }
    cx += GLYPH_WIDTH + GLYPH_SPACING;
  }
}
